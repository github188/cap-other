    private String height;
    
    /** 手机 */
    private String phone;
    
    /** 毕业学校 */
    private String school;
    
    /** 学历 */
    private String degree;
    
    /** 专业 */
    private String major;
    
    /** E-Mail */
    private String email;
    
    /** 现居住地 */
    private String address;
    
    /** 其他信息（嵌入Excel） */
    private String other;
    
